# ------------------------------------------------------------------------------
# 3. SUBGROUP DISCRIMINATION USING FINAL XGBOOST MODEL
# ------------------------------------------------------------------------------

subgroup_df <- xgb_preds %>%
  transmute(
    ID = as.character(ID),
    y_test = as.integer(y_test),
    final_pred = as.numeric(.data[[FINAL_XGB_PRED_COL]])
  )

df_anno <- df0 %>%
  mutate(ID = if (ID_COL %in% names(.)) as.character(.data[[ID_COL]]) else as.character(seq_len(nrow(.)))) %>%
  transmute(
    ID,
    sex = if (SEX_COL %in% names(df0)) as.factor(.data[[SEX_COL]]) else NA,
    apoe4 = if (APOE4_COL %in% names(df0)) as.factor(.data[[APOE4_COL]]) else NA,
    age_onset = if (AGE_ONSET_COL %in% names(df0)) suppressWarnings(as.numeric(.data[[AGE_ONSET_COL]])) else NA_real_
  )

subgroup_df <- subgroup_df %>%
  left_join(df_anno, by = "ID")

auc_rows <- list()
auprc_rows <- list()

# Overall
v <- auc_ci_delong(subgroup_df$y_test, subgroup_df$final_pred)
auc_rows[["Overall"]] <- v %>%
  mutate(Analysis = "Overall", Group = "All participants", .before = 1)

auprc_rows[["Overall"]] <- tibble(
  Analysis = "Overall",
  Group = "All participants",
  AUPRC = auprc_value(subgroup_df$y_test, subgroup_df$final_pred)
)

# Sex subgroup
if (!all(is.na(subgroup_df$sex))) {
  for (s in levels(droplevels(subgroup_df$sex))) {
    dd <- subgroup_df %>% filter(sex == s)
    key <- paste0("Sex_", s)

    auc_rows[[key]] <- auc_ci_delong(dd$y_test, dd$final_pred) %>%
      mutate(Analysis = "Sex", Group = as.character(s), .before = 1)

    auprc_rows[[key]] <- tibble(
      Analysis = "Sex",
      Group = as.character(s),
      AUPRC = auprc_value(dd$y_test, dd$final_pred)
    )
  }
}

# APOE-e4 subgroup
if (!all(is.na(subgroup_df$apoe4))) {
  for (g in levels(droplevels(subgroup_df$apoe4))) {
    dd <- subgroup_df %>% filter(apoe4 == g)
    key <- paste0("APOE4_", g)

    auc_rows[[key]] <- auc_ci_delong(dd$y_test, dd$final_pred) %>%
      mutate(Analysis = "APOE-e4 status", Group = as.character(g), .before = 1)

    auprc_rows[[key]] <- tibble(
      Analysis = "APOE-e4 status",
      Group = as.character(g),
      AUPRC = auprc_value(dd$y_test, dd$final_pred)
    )
  }
}

# Age-at-onset subgroup among cases versus all non-cases
if (!all(is.na(subgroup_df$age_onset))) {
  dd_early <- subgroup_df %>%
    mutate(early_case = ifelse(!is.na(age_onset) & age_onset < 65, 1, 0)) %>%
    filter(y_test == 0 | early_case == 1)

  auc_rows[["EarlyOnset"]] <- auc_ci_delong(dd_early$y_test, dd_early$final_pred) %>%
    mutate(Analysis = "Age at onset", Group = "Early-onset ADRD (<65)", .before = 1)

  auprc_rows[["EarlyOnset"]] <- tibble(
    Analysis = "Age at onset",
    Group = "Early-onset ADRD (<65)",
    AUPRC = auprc_value(dd_early$y_test, dd_early$final_pred)
  )

  dd_late <- subgroup_df %>%
    mutate(late_case = ifelse(!is.na(age_onset) & age_onset >= 65, 1, 0)) %>%
    filter(y_test == 0 | late_case == 1)

  auc_rows[["LateOnset"]] <- auc_ci_delong(dd_late$y_test, dd_late$final_pred) %>%
    mutate(Analysis = "Age at onset", Group = "Late-onset ADRD (>=65)", .before = 1)

  auprc_rows[["LateOnset"]] <- tibble(
    Analysis = "Age at onset",
    Group = "Late-onset ADRD (>=65)",
    AUPRC = auprc_value(dd_late$y_test, dd_late$final_pred)
  )
}

auc_subgroups <- bind_rows(auc_rows)
auprc_subgroups <- bind_rows(auprc_rows)

subgroup_summary <- auc_subgroups %>%
  left_join(auprc_subgroups, by = c("Analysis", "Group"))

write_csv(
  subgroup_summary,
  file.path(OUT_DIR, "FigureA9_subgroup_auc_auprc_summary.csv")
)

