predict_risk_vec <- function(fit, newdata, times) {
  r <- riskRegression::predictRisk(fit, newdata = newdata, times = times)
  as.numeric(if (is.matrix(r)) r[, 1] else r)
}

compute_group_shr <- function(df, group_var) {
  mm <- model.matrix(reformulate(group_var), data = df)[, -1, drop = FALSE]

  fit <- cmprsk::crr(
    ftime = df$time,
    fstatus = df$event,
    cov1 = mm,
    failcode = 1,
    cencode = 0
  )

  beta <- as.numeric(fit$coef)
  se <- sqrt(diag(fit$var))

  tibble(
    sHR = exp(beta),
    CI_low = exp(beta - 1.96 * se),
    CI_high = exp(beta + 1.96 * se),
    p_value = 2 * pnorm(-abs(beta / se))
  )
}

compute_continuous_shr_per1pct <- function(df, risk_vec) {
  cov_mat <- matrix(risk_vec, ncol = 1)

  fit <- cmprsk::crr(
    ftime = df$time,
    fstatus = df$event,
    cov1 = cov_mat,
    failcode = 1,
    cencode = 0
  )

  beta <- as.numeric(fit$coef)
  se <- sqrt(diag(fit$var))

  tibble(
    sHR_per_1pct = exp(beta * 0.01),
    CI_low = exp((beta - 1.96 * se) * 0.01),
    CI_high = exp((beta + 1.96 * se) * 0.01),
    p_value = 2 * pnorm(-abs(beta / se))
  )
}

make_cif_df <- function(time, event, group) {
  ci <- cmprsk::cuminc(ftime = time, fstatus = event, group = group)
  cause1_names <- names(ci)[grepl(" 1$", names(ci))]

  bind_rows(lapply(cause1_names, function(nm) {
    e <- ci[[nm]]
    grp <- sub(" 1$", "", nm)

    tibble(
      time = e$time,
      est = as.numeric(e$est),
      var = as.numeric(e$var),
      group = grp
    )
  })) %>%
    mutate(
      se = sqrt(var),
      lower = pmax(0, est - 1.96 * se),
      upper = pmin(1, est + 1.96 * se),
      group = factor(group, levels = c("Lower 75%", "Top 25%"))
    )
}

auc_ci_delong <- function(y, p) {
  ok <- !is.na(y) & !is.na(p)
  y <- y[ok]
  p <- p[ok]

  if (length(unique(y)) < 2) {
    return(tibble(AUC = NA_real_, Lower_CI = NA_real_, Upper_CI = NA_real_, N = length(y), Cases = sum(y == 1)))
  }

  roc_obj <- pROC::roc(
    response = y,
    predictor = p,
    levels = c(0, 1),
    direction = "auto",
    quiet = TRUE
  )

  ci <- as.numeric(pROC::ci.auc(roc_obj, method = "delong"))

  tibble(
    AUC = as.numeric(pROC::auc(roc_obj)),
    Lower_CI = ci[1],
    Upper_CI = ci[3],
    N = length(y),
    Cases = sum(y == 1)
  )
}

auprc_value <- function(y, p) {
  ok <- !is.na(y) & !is.na(p)
  y <- y[ok]
  p <- p[ok]

  if (length(unique(y)) < 2) return(NA_real_)

  pr <- PRROC::pr.curve(
    scores.class0 = p[y == 1],
    scores.class1 = p[y == 0],
    curve = FALSE
  )

  as.numeric(pr$auc.integral)
}

