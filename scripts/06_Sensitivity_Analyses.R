# 1. HORIZON ROBUSTNESS
# ------------------------------------------------------------------------------

shr_rows <- list()
threshold_rows <- list()
plot_list <- list()

for (nm in names(HORIZONS)) {
  t_days <- as.numeric(HORIZONS[[nm]])

  risk_vec <- predict_risk_vec(fg_fit, newdata = test_surv, times = t_days)
  q75 <- as.numeric(quantile(risk_vec, probs = HIGH_RISK_Q, na.rm = TRUE))

  group_vec <- factor(
    ifelse(risk_vec > q75, "Top 25%", "Lower 75%"),
    levels = c("Lower 75%", "Top 25%")
  )

  df_tmp <- test_surv %>%
    mutate(
      risk_horizon = risk_vec,
      risk_group = group_vec
    )

  shr_group <- compute_group_shr(df_tmp, "risk_group")
  shr_cont <- compute_continuous_shr_per1pct(df_tmp, risk_vec)

  event_summary <- df_tmp %>%
    group_by(risk_group) %>%
    summarise(
      n = n(),
      n_ADRD = sum(event == 1),
      n_death = sum(event == 2),
      n_censored = sum(event == 0),
      ADRD_percent = 100 * n_ADRD / n(),
      .groups = "drop"
    )

  write_csv(
    event_summary,
    file.path(OUT_DIR, paste0("riskgroup_event_summary_", nm, ".csv"))
  )

  shr_rows[[nm]] <- tibble(
    Horizon = nm,
    Days = t_days,
    Group_sHR = shr_group$sHR,
    Group_CI_low = shr_group$CI_low,
    Group_CI_high = shr_group$CI_high,
    Group_p = shr_group$p_value,
    Continuous_sHR_per_1pct = shr_cont$sHR_per_1pct,
    Continuous_CI_low = shr_cont$CI_low,
    Continuous_CI_high = shr_cont$CI_high,
    Continuous_p = shr_cont$p_value
  )

  threshold_rows[[nm]] <- tibble(
    Horizon = nm,
    Days = t_days,
    q75_threshold = q75
  )

  cif_df <- make_cif_df(df_tmp$time, df_tmp$event, df_tmp$risk_group)

  ann <- paste0(
    "sHR = ", sprintf("%.2f", shr_group$sHR),
    " (", sprintf("%.2f", shr_group$CI_low),
    "-", sprintf("%.2f", shr_group$CI_high), ")"
  )

  plot_list[[nm]] <- ggplot(cif_df, aes(x = time / 365.25, y = est, group = group)) +
    geom_ribbon(aes(ymin = lower, ymax = upper), alpha = 0.18) +
    geom_step(linewidth = 0.9) +
    scale_y_continuous(labels = percent_format(accuracy = 1)) +
    labs(
      title = paste0("Cumulative incidence by predicted-risk group: ", nm),
      subtitle = ann,
      x = "Follow-up time, years",
      y = "Cumulative incidence"
    ) +
    theme_classic(base_size = 12) +
    theme(
      legend.position = "top",
      plot.title = element_text(face = "bold", hjust = 0.5),
      plot.subtitle = element_text(hjust = 0.5)
    )
}

shr_by_horizon <- bind_rows(shr_rows)
thresholds_by_horizon <- bind_rows(threshold_rows)

write_csv(shr_by_horizon, file.path(OUT_DIR, "shr_by_horizon_test.csv"))
write_csv(thresholds_by_horizon, file.path(OUT_DIR, "riskgroup_thresholds_by_horizon.csv"))

